-- Question 5:
-- Find the percentage distribution of feedback from the customers. Are customers getting more dissatisfied over time? [5 marks]
-- Hint: Calculate the percentage of each feedback type by using conditional aggregation. 
-- For each feedback category, use a CASE statement to count the occurrences and then divide by the total count of feedback for the quarter, multiplied by 100 to get the percentage. 
-- Finally, group by quarter_number and order the results to reflect the correct sequence.
-- select max(order_date), min(order_date) from order_t;

select
    quarter_number,
    (sum(CASE WHEN customer_feedback = 'Very Bad' THEN 1 ELSE 0 END) / count(customer_feedback)) * 100 AS "Very_Bad(%)",
    (sum(CASE WHEN customer_feedback = 'Bad' THEN 1 ELSE 0 END) / count(customer_feedback)) * 100 AS "Bad(%)",
    (sum(CASE WHEN customer_feedback = 'Okay' THEN 1 ELSE 0 END) / count(customer_feedback)) * 100 AS "Okay(%)",
    (sum(CASE WHEN customer_feedback = 'Good' THEN 1 ELSE 0 END) / count(customer_feedback)) * 100 AS "Good(%)",
    (sum(CASE WHEN customer_feedback = 'Very Good' THEN 1 ELSE 0 END) / count(customer_feedback)) * 100 AS "Very_Good(%)"
from order_t
group by quarter_number
order by quarter_number;


