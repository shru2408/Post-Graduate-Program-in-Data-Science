-- Question 2:
-- Which are the top 5 vehicle makers preferred by the customers? [4 marks]
-- Hint: For each vehicle make what is the count of the customers.
select 
p.vehicle_maker , count(o.customer_id) as Total_customer
from order_t o join product_t p on p.product_id=o.product_id 
group by p.vehicle_maker order by Total_customer desc limit 5;




-- Question 3:
-- Which is the most preferred vehicle maker in each state? [4 marks]
-- Hint: Use the window function RANK() to rank based on the count of customers for each state and vehicle maker. 
-- After ranking, take the vehicle maker whose rank is 1.

select * from (
SELECT 
	c.state,
	p.vehicle_maker,count(*) AS Total_customer,
	RANK() over (partition by state order by count(c.customer_id) desc) as ranks
FROM order_t o JOIN product_t p on p.product_id=o.product_id
INNER JOIN customer_t c on c.customer_id = o.customer_id
GROUP BY c.state,p.vehicle_maker
) preferred_vehicle
where ranks =1
order by Total_customer desc;



-- Question 4:
-- Find the overall average rating given by the customers. What is the average rating in each quarter? [5 marks]
-- Consider the following mapping for ratings:
-- “Very Bad”: 1, “Bad”: 2, “Okay”: 3, “Good”: 4, “Very Good”: 5
-- Hint: Use subquery and assign numerical values to feedback categories using a CASE statement. 
-- Then, calculate the average feedback count per quarter. Use a subquery to convert feedback 
-- into numerical values and group by quarter_number to compute the average.

select quarter_number , round(avg(customer_feedback_num),2) as average_feedback from (
select  
        quarter_number, (CASE customer_feedback
        WHEN 'Very Bad' THEN 1
        WHEN 'Bad' THEN 2
        WHEN 'Okay' THEN 3
        WHEN 'Good' THEN 4
        WHEN 'Very Good' THEN 5
    END) AS customer_feedback_num 
    FROM order_t 
 ) order_tb
 group by quarter_number order by quarter_number;
